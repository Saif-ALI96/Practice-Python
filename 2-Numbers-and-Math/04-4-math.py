#pour pouvoir l'ustiliser, on peut l'appeler de la maniere suivente :

from math import *

#les functions suiventes apprtient a cette bibliotheque qui se trouve ce-dessus

print(floor(3.9)) #----> nous aurons le chiffre (3)
print(floor(7.9)) #----> nous aurons le chiffre (7)
#Il imprimera le plus petit nombre des nombres décimaux à l'intérieur des parenthèses

print(ceil(8.2)) #---> nous aurons le chiffre (9)
print(ceil(4.5)) #---> nous aurons le chiffre (5)
# Il imprime le plus grand nombre des nombres décimaux à l'intérieur des parenthèses
# et passe a la prochain chiffre 

print(max(2,9)) # -----> (9)
print(min(-1,-2)) #----> (-2)
# Cette fonction renvoie le maximum ou minimum des valeurs passées en paramètres


print(abs(-1)) # ---> nous aurons le chiffre positive (+1)
print(abs(+1)) #---> nous aurons le chiffre positif (+1)
print(abs(0)) #---> nous aurons le chiffre nulle (0)

#Cette fonction retourne un nombre absolu (un nombre sans signe)



print(pow(2,3)) #---> nous aurons le chiffre float (8.0)
print(2**3) #---> c'est le meme chose, nous aurons le chiffre pas float mais plutot integer (8)
print(pow(6,2)) #---> nous aurons le chiffre float (36.0)

#Cette function permet de calculer une puissance (الاسس) entre deux nombres


print(sqrt(9)) #---> nous aurons le chiffre float (3.0)
print(sqrt(16)) #---> nous aurons le chiffre float (4.0)

#Cette function calcule la racine carrée (الجذر التربيعي) du nombre donné en paramètre


print(log(10)) #---> nous aurons le chiffre float (2.3025.....)
print(log(100)) #---> nous aurons le chiffre float (4.6051701...)

#Cette function calcule le logarithm naturel (الخطوة المادية) du nombre donné en paramètre


print(sin(pi/2)) #---> nous aurons le chiffre float (1.0)
print(cos(pi/1)) #---> nous aurons le chiffre float (-1.0)
print(tan(pi/2)) #---> nous aurons le chiffre float (1.633123935319537e+16)

